    /**
     * @throws \Exception
     */
    public function amocrmUpdate(): void
    {
        $amoApi = (new Client(Auth::user()->account))->init();

        if ($amoApi->auth !== false) {

            Account::users($amoApi);
            Account::statuses($amoApi);

            Notification::make()
                ->title('Успешно обновлено')
                ->success()
                ->send();

        } else
            Redirect::to('https://www.amocrm.ru/oauth/?state=hello&mode=popup&client_id='.config('services.amocrm.client_id').'&uuid='.Auth::user()->uuid);
//            Notification::make()
